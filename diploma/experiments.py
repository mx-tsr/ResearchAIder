def compute_penalty_from_issues(issues):
    if not isinstance(issues, list):
        return 0

    has_critical = any(
        isinstance(issue, dict) and issue.get("type") == "critical"
        for issue in issues
    )
    has_major = any(
        isinstance(issue, dict) and issue.get("type") == "major"
        for issue in issues
    )
    minor_count = sum(
        1
        for issue in issues
        if isinstance(issue, dict) and issue.get("type") == "minor"
    )

    penalty = 0
    if has_critical:
        penalty -= 3
    if has_major:
        penalty -= 2
    penalty -= minor_count // 3

    return penalty


def normalize_section_evaluation(section_result):
    if not isinstance(section_result, dict):
        return {
            "section": None,
            "score": 0,
            "issues": [],
            "final_reasoning": "Не удалось распарсить результат LLM."
        }

    issues = section_result.get("issues") or []
    if not isinstance(issues, list):
        issues = []

    raw_score = section_result.get("score")
    try:
        raw_score = int(raw_score)
    except (TypeError, ValueError):
        raw_score = 0

    penalty = compute_penalty_from_issues(issues)
    adjusted_score = max(1, min(10, raw_score + penalty))

    section_result["issues"] = issues
    section_result["raw_score"] = raw_score
    section_result["penalty_adjustment"] = penalty
    section_result["score"] = adjusted_score
    section_result["score_explanation"] = (
        f"raw_score={raw_score}, penalty={penalty}, final_score={adjusted_score}"
    )

    return section_result


